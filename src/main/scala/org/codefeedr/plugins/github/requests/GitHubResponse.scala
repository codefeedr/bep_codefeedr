package org.codefeedr.plugins.github.requests

/**
  * Represents a response generated by a request to the GitHub API.
  * @param body the body of the response.
  * @param status the status of the response.
  * @param headers the headers of the response.
  */
case class GitHubResponse(body: String,
                             status: Int,
                             headers: List[Header])

/**
  * Represents a header.
  * @param key the header key.
  * @param value the header values.
  */
case class Header(key: String, value: Array[String])
